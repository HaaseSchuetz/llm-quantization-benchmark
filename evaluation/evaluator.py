import json
from typing import Dict, List, Optional, Union
from dataclasses import dataclass
import pandas as pd
from lm_evaluation_harness import evaluate as lm_evaluate
from transformers import PreTrainedModel, PreTrainedTokenizer

@dataclass
class EvaluationResult:
    model: str
    quantization: str
    benchmark: str
    metrics: Dict[str, float]
    raw_results: Optional[Dict] = None

class LLEvaluator:
    """Evaluate quantized LLMs on standard benchmarks."""

    def __init__(
        self,
        benchmarks_config: str = "config/benchmarks.json",
        results_dir: str = "results"
    ):
        self.benchmarks_config = self._load_json(benchmarks_config)
        self.results_dir = results_dir

    @staticmethod
    def _load_json(path: str) -> Dict:
        with open(path) as f:
            return json.load(f)

    def evaluate_model(
        self,
        model: PreTrainedModel,
        tokenizer: PreTrainedTokenizer,
        model_name: str,
        quantization: str,
        benchmark_name: str,
        limit: Optional[int] = None,
        save_raw: bool = True
    ) -> EvaluationResult:
        """Evaluate a quantized model on a benchmark."""
        benchmark_config = self.benchmarks_config[benchmark_name]

        # Prepare evaluation args
        eval_args = {
            "model": "huggingface",
            "model_args": f"pretrained={model_name}",
            "tasks": [benchmark_config["task"]],
            "num_fewshot": benchmark_config["num_fewshot"],
            "batch_size": "auto",
            "device": "cuda",
            "limit": limit,
            "write_out": False,
        }

        # Run evaluation
        results = lm_evaluate(**eval_args)

        # Extract metrics
        metrics = {}
        task = benchmark_config["task"]
        if task in results["results"]:
            task_results = results["results"][task]
            for metric in benchmark_config["metrics"]:
                if metric in task_results:
                    if isinstance(task_results[metric], dict):
                        metric_key = next(iter(task_results[metric]))
                        metrics[metric] = task_results[metric][metric_key]
                    else:
                        metrics[metric] = task_results[metric]

        # Save raw results
        if save_raw:
            import os
            from pathlib import Path
            raw_dir = Path(self.results_dir) / "raw"
            raw_dir.mkdir(parents=True, exist_ok=True)
            raw_path = raw_dir / f"{model_name.replace('/', '_')}_{quantization}_{benchmark_name}.json"
            with open(raw_path, "w") as f:
                json.dump(results, f, indent=2)

        return EvaluationResult(
            model=model_name,
            quantization=quantization,
            benchmark=benchmark_name,
            metrics=metrics,
            raw_results=results
        )

    def generate_report(
        self,
        results: List[EvaluationResult],
        output_format: str = "markdown"
    ) -> str:
        """Generate a report from evaluation results."""
        data = []
        for result in results:
            for metric, value in result.metrics.items():
                data.append({
                    "Model": result.model,
                    "Quantization": result.quantization,
                    "Benchmark": result.benchmark,
                    "Metric": metric,
                    "Value": value
                })

        df = pd.DataFrame(data)

        if output_format == "markdown":
            pivot_df = df.pivot_table(
                index=["Model", "Quantization", "Benchmark"],
                columns="Metric",
                values="Value"
            ).reset_index()

            for col in pivot_df.columns:
                if col not in ["Model", "Quantization", "Benchmark"]:
                    pivot_df[col] = pivot_df[col].apply(lambda x: f"{x:.4f}" if isinstance(x, (int, float)) else x)

            md_table = pivot_df.to_markdown(index=False)
            return f"# 📊 Quantization Benchmark Report\n\n{md_table}\n\n---\n*Generated by [llm-quantization-benchmark](https://github.com/HaaseSchuetz/llm-quantization-benchmark)*"

        elif output_format == "csv":
            return df.to_csv(index=False)

        elif output_format == "json":
            return df.to_json(orient="records", indent=2)

        else:
            raise ValueError(f"Unsupported format: {output_format}")
