import argparse
import json
import logging
from pathlib import Path
from typing import Dict, List
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from config.logging_config import setup_logging

logger = logging.getLogger(__name__)

def load_results(results_dir: str = "results/raw") -> List[Dict]:
    """Load all benchmark results from the results directory."""
    results = []
    raw_dir = Path(results_dir)
    for file in raw_dir.glob("*_quantization_benchmark.json"):
        with open(file) as f:
            data = json.load(f)
        results.extend(data)
    return results

def generate_comparison_report(
    results: List[Dict],
    output_format: str = "markdown",
    filename: str = "quantization_comparison"
) -> str:
    """Generate a comparison report from benchmark results."""
    # Flatten efficiency metrics
    efficiency_data = []
    accuracy_data = []

    for result in results:
        model = result["model"]
        quantization = result["quantization"]
        q_bits = result["config"]["bits"]
        q_name = result["config"]["name"]

        # Efficiency metrics
        efficiency = result["efficiency"]
        efficiency_data.append({
            "Model": model,
            "Quantization": q_name,
            "Bits": q_bits,
            "Model Size (MB)": efficiency["memory"]["model_size_mb"],
            "GPU Memory Used (MB)": efficiency["memory"]["gpu_used_mb"],
            "Avg Latency (ms)": efficiency["latency"]["avg_latency_ms"],
            "Tokens/sec": efficiency["token_speed"]
        })

        # Accuracy metrics
        for acc_result in result["accuracy"]:
            benchmark = acc_result["benchmark"]
            for metric, value in acc_result["metrics"].items():
                accuracy_data.append({
                    "Model": model,
                    "Quantization": q_name,
                    "Bits": q_bits,
                    "Benchmark": benchmark,
                    "Metric": metric,
                    "Value": value
                })

    # Generate reports
    if output_format == "markdown":
        # Efficiency report
        eff_df = pd.DataFrame(efficiency_data)
        eff_md = eff_df.to_markdown(index=False)

        # Accuracy report
        acc_df = pd.DataFrame(accuracy_data)
        acc_md = acc_df.pivot_table(
            index=["Model", "Quantization", "Bits"],
            columns=["Benchmark", "Metric"],
            values="Value"
        ).to_markdown()

        report = f"""# LLM Quantization Comparison Report

## Efficiency Metrics
{eff_md}

## Accuracy Metrics
{acc_md}

---
*Generated by [llm-quantization-benchmark](https://github.com/HaaseSchuetz/llm-quantization-benchmark)*
"""
        return report

    elif output_format == "csv":
        eff_df = pd.DataFrame(efficiency_data)
        acc_df = pd.DataFrame(accuracy_data)
        return {
            "efficiency": eff_df.to_csv(index=False),
            "accuracy": acc_df.to_csv(index=False)
        }

    elif output_format == "json":
        return json.dumps({
            "efficiency": efficiency_data,
            "accuracy": accuracy_data
        }, indent=2)

    else:
        raise ValueError(f"Unsupported format: {output_format}")

def save_report(report: str, filename: str, output_format: str, save_dir: str = "results/reports"):
    """Save a report to file."""
    save_path = Path(save_dir)
    save_path.mkdir(parents=True, exist_ok=True)

    ext = {
        "markdown": "md",
        "csv": "csv",
        "json": "json"
    }[output_format]

    if output_format == "markdown":
        filepath = save_path / f"{filename}.{ext}"
        with open(filepath, "w") as f:
            f.write(report)
    else:
        # For CSV/JSON, save efficiency and accuracy separately
        if isinstance(report, dict):
            for key, content in report.items():
                filepath = save_path / f"{filename}_{key}.{ext}"
                with open(filepath, "w") as f:
                    f.write(content)
        else:
            filepath = save_path / f"{filename}.{ext}"
            with open(filepath, "w") as f:
                f.write(report)

    logger.info(f"Report saved to: {save_path}")
    return filepath

def plot_results(results: List[Dict], save_dir: str = "results/reports"):
    """Generate plots from benchmark results."""
    # Prepare data
    efficiency_data = []
    accuracy_data = []

    for result in results:
        model = result["model"]
        quantization = result["quantization"]
        q_bits = result["config"]["bits"]
        q_name = result["config"]["name"]

        # Efficiency
        efficiency = result["efficiency"]
        efficiency_data.append({
            "Model": model,
            "Quantization": q_name,
            "Bits": q_bits,
            "Model Size (MB)": efficiency["memory"]["model_size_mb"],
            "GPU Memory (MB)": efficiency["memory"]["gpu_used_mb"],
            "Latency (ms)": efficiency["latency"]["avg_latency_ms"],
            "Tokens/sec": efficiency["token_speed"]
        })

        # Accuracy
        for acc_result in result["accuracy"]:
            benchmark = acc_result["benchmark"]
            for metric, value in acc_result["metrics"].items():
                accuracy_data.append({
                    "Model": model,
                    "Quantization": q_name,
                    "Bits": q_bits,
                    "Benchmark": benchmark,
                    "Value": value
                })

    # Plot 1: Model Size vs. Accuracy
    plt.figure(figsize=(12, 6))
    eff_df = pd.DataFrame(efficiency_data)
    acc_df = pd.DataFrame(accuracy_data)

    # Merge data
    merged_df = pd.merge(
        eff_df,
        acc_df,
        on=["Model", "Quantization", "Bits"]
    )

    sns.scatterplot(
        data=merged_df,
        x="Model Size (MB)",
        y="Value",
        hue="Bits",
        style="Quantization",
        size="Bits",
        sizes=(100, 200, 300),
        alpha=0.7
    )
    plt.title("Model Size vs. Accuracy (Lower Size = Better, Higher Accuracy = Better)")
    plt.ylabel("Accuracy")
    plt.grid(True)
    plt.tight_layout()
    plt.savefig(save_dir + "/size_vs_accuracy.png", dpi=300, bbox_inches="tight")
    plt.close()

    # Plot 2: Latency vs. Accuracy
    plt.figure(figsize=(12, 6))
    sns.scatterplot(
        data=merged_df,
        x="Latency (ms)",
        y="Value",
        hue="Bits",
        style="Quantization",
        size="Bits",
        sizes=(100, 200, 300),
        alpha=0.7
    )
    plt.title("Latency vs. Accuracy (Lower Latency = Better, Higher Accuracy = Better)")
    plt.ylabel("Accuracy")
    plt.grid(True)
    plt.tight_layout()
    plt.savefig(save_dir + "/latency_vs_accuracy.png", dpi=300, bbox_inches="tight")
    plt.close()

    # Plot 3: Throughput vs. Accuracy
    plt.figure(figsize=(12, 6))
    sns.scatterplot(
        data=merged_df,
        x="Tokens/sec",
        y="Value",
        hue="Bits",
        style="Quantization",
        size="Bits",
        sizes=(100, 200, 300),
        alpha=0.7
    )
    plt.title("Throughput vs. Accuracy (Higher Throughput = Better, Higher Accuracy = Better)")
    plt.ylabel("Accuracy")
    plt.grid(True)
    plt.tight_layout()
    plt.savefig(save_dir + "/throughput_vs_accuracy.png", dpi=300, bbox_inches="tight")
    plt.close()

    logger.info(f"Plots saved to: {save_dir}")

def main():
    parser = argparse.ArgumentParser(description="Compare quantization methods.")
    parser.add_argument("--results-dir", type=str, default="results/raw", help="Directory with benchmark results")
    parser.add_argument("--format", type=str, default="markdown", choices=["markdown", "csv", "json"])
    parser.add_argument("--filename", type=str, default="quantization_comparison")
    parser.add_argument("--plot", action="store_true", help="Generate plots")
    parser.add_argument("--log-level", type=str, default="INFO", choices=["DEBUG", "INFO", "WARNING", "ERROR", "CRITICAL"], help="Logging level")
    parser.add_argument("--log-file", type=str, default=None, help="Log file path (optional)")
    args = parser.parse_args()

    # Setup logging
    setup_logging(level=args.log_level, log_file=args.log_file)

    # Load results
    results = load_results(args.results_dir)

    # Generate report
    report = generate_comparison_report(results, args.format, args.filename)
    save_report(report, args.filename, args.format)

    # Generate plots
    if args.plot:
        plot_results(results)

    logger.info("Comparison Report:")
    if args.format == "markdown":
        print(report)

if __name__ == "__main__":
    main()
